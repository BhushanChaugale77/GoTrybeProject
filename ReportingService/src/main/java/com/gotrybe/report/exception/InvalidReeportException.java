package com.gotrybe.report.exception;

public class InvalidReeportException extends RuntimeException {

	public InvalidReeportException() {
		super();
 	}

	public InvalidReeportException(String message, Throwable cause) {
		super(message, cause);
 	}

	public InvalidReeportException(String message) {
		super(message);
 	}

	public InvalidReeportException(Throwable cause) {
		super(cause);
 	}

	
	
}
